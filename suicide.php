<p>Suicide isn't just a word you 
    use lightly. It isn't passed around nilly willy either. Having been on the 
    "wanting to end it" side of suicide I know how it 
    is to have such dark thoughts that you want to end it all. It's not an easy
    place to be. It's not a fun place to be either.
</p>

<p>Luckily I have an amazing wife who could see the problems I was facing and she stepped in
    and got me the help I needed. I stayed in a hospital for a week. After that week, I was
    released. I was home for a week and things still weren't the best. So I went back for
    another week. That second week helped me and when I was released a second time, I
    was able to relax again knowing that I was on medication and had a good support system there
    able to help me.
</p>

<p>It's not an easy thing to talk about. The "S" word as I call it. It's just...difficult.
    I don't like admitting that I had gotten to such a deep dark place that I was unable
    to function properly. To recognize that I needed help was the first step. To actually
    act on that need for help was something entirely different. I don't know if I could have
    gotten through it without the help of my wife. Someone on the outside seeing in and going
    "hey, something's not right there." It was an eye opening experience.
</p>

