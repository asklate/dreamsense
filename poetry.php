<h2>Poetry To Make My Head Spin</h2>

<p>Sometimes poetry is the only way you have to communicate a thought.</p>

<h3>Thoughts In The World</h3>

    <p><strong>Had I</strong> a thought to share with the world<br/>
    I would share it long and wide<br/>
    perhaps a difficult task to do<br/>
    but whoever knows it's alive.</p>

    <p><strong>Tis but</strong> a life upon which<br/>
    the world can burn<br/>
    if only you saw<br/>
    everything there was to see.</p>

    <p><strong>But how</strong> in the world<br/>
    does one find all they need<br/>
    in a manner upon which<br/>
    it makes sense.</p>

<hr/>

<h3>Nothing Exists Without Reason</h3>

    <p><strong>There are</strong> things in this world<br/>
    that are around for a reason<br/>
    other things display<br/>
    without your control.</p>

    <p><strong>Don't allow</strong> anything<br/>
    to get in your way<br/>
    they don't matter<br/>
    as much as you think.</p>

<hr/>

<h3>Making Mistakes</h3>

    <p><strong>Mistakes, they</strong> come and go<br/>
    I don't know where they come or where they end up<br/>
    but I do know they can stick around for a while<br/>
    life tends to be that way in the long run.</p>

    <p><strong>So you</strong> need to allow for mistakes<br/>
    to happen, they'll happen regardless of what you try<br/>
    you have to step up and accept them for what they are<br/>
    and simply move on.</p>

<hr/>

<h3>Life In A Nutshell</h3>

    <p><strong>How did</strong> life become a nutshell?<br/>
    It tried its best not to, it tried its best to survive<br/>
    yet here we are in this nutshell of a life<br/>
    there seems to be no way out.</p>

    <p><strong>In order</strong> to live we must be valiant<br/>
    Yes valiant to the very end!<br/>
    Why you ask? Why not I say.<br/>
    If only you could see what I see.</p>

<hr/>

<h3>A Dream</h3>

    <p><strong>Had I</strong> a dream, I would share it with<br/>
    everyone out there in the world<br/>
    if but a dream existed<br/>
    and there was a world to share.</p>

