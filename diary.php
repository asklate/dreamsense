<h2>Some Sense</h2>

<p>I found out a little too late that writing in a diary wasn't the best option
    for me. Sometimes the voices in my head would find their way onto the paper.
    I've had psychologists tell me that wasn't a good sign and that I should stop
    writing down whatever popped into my head. Even though they first told me it
    didn't matter what I wrote just as long as I wrote. It seemed to get a little
    confusing.</p>

<p>Since that time, I've tried to just write stories. Eventually hoping one of those
    stories would turn into a novel. Or some lengthy piece of work. I've written some
    books over the course of my years of life. Mostly for my own enjoyment. There doesn't
    seem to be anything else to write but stories these days. So I persist and see what
    comes out of my pen.</p>

<p>Yeah I tend to write on paper still. I feel I get the most out of it when I do that.
    After I write it on paper, that's my first draft, I type it up and make changes as
    I go, that becomes my second draft. Then I polish it as needed which becomes my third
    draft. It seems to work for me so far, and I don't have a problem with that.</p>

