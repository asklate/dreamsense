<h2>So This Is Life</h2>

<p>Life can come at you in so many different ways.</p>

<p>Sometimes you don't want to see how it's coming at you. You want to run and hide from it all.
    You don't even want to care about how it's coming at you. That's how terrible it can feel
    at times. It's not always like that, but some days it is. I'm not saying that's how it always
    is of course. So don't be trying to take <em>that</em> from this.</p>

<p>20 Years this website has been in existence. It has gone through many iterations. I never have
    figured out the exact format I want it in. Not yet. I suppose that's what life does to you.
</p>

<p>Sometimes it's not <a href="easy.php" title="Life Isn't Always Easy">easy</a> to live in this life, that much is certain. 
    Yet here we are hoping everything will work out in the end and we will be able to figure 
    it all out.
</p>

<p>There's a word that defines me the most, that's <a href="anxiety.php" title="Anxiety">anxiety</a>. 
    It's a pain in the butt.
</p>

<p>The <a href="end.php" title="The End Of Life">end</a> of one's life can be a quick thing or it can take a while.
    What is it about <a href="death.php" title="Death">death</a> that makes people uneasy? It would seem 
    like a natural part of life. Would it not? I get it you don't want your loved one to go
    away and all of that. But sometimes it's just time. But people are afraid to die themselves.
</p>

<p>This life is ever so fragile. Everyone has their vulnerabilities. Some may not want to show those
    to other people and that's okay, but everyone has them. That's just how this life works out for
    the most part.
</p>

<p>Sometimes thinking about life can drive you up the wall. Never knowing what to expect sounds like
    it's a good reason to <a href="worry.php" title="Worry">worry</a> about things for certain. It
    could be worse you could be hearing <a href="voices.php" title="Voices">voices</a> and what not. 
    Now that's a crazy time to be had I know I've experienced it firsthand.
</p>

<p>Then there are <a href="visual.php" title="Visual Hallucinations">visual hallucinations</a>. Those 
    are where you see things that aren't there.
</p>

<p>Speaking of crazy. Is it possible for me to call myself crazy but not like it when other people
    call me that? Is that a thing? It sounds kind of petty at first. But in a way I am crazy to a degree.
    Sure everyone we know is crazy to a specific degree/setting in life. But to just blurt out that
    they are <a href="crazy.php" title="Crazy">crazy</a> seems to be quite a turn around.
</p>

<p>I wonder at times if this life even matters. Does it matter to me? Does it matter to other people?
    who exactly does this life matter to? I don't understand. If it matters to someone, why doesn't it
    really matter to me? Maybe that's the question I should be asking.
</p>

<p>There are times the <a href="thoughts.php" title="Over Thinking">thoughts</a> can be too much for me.
    They stick around because there's no place else for them to go I think.
</p>

