<p>There are times when I worry.</p>

<p><a href="/images/worry.jpg"><img src="/images/worry.jpg" height="100" width="100" alt="Worry" title="worry" class="left"/></a>
    I was reading on Reddit one day, what are some things that you worry about the most.
    If it's a constant worry, then that's an easy answer. Everything.
    Yeah, I tend to worry about everything in the universe it would seem.
    I don't know <em>why</em> I worry about things like that. It's crazy. I wish I had
    an idea of why I tend to worry about things. I mean wouldn't it be a good thing to
    have that all figured out? Yeah I was thinking so.
</p>

<p>It would be easier not to worry about things, not to have anxiety about things.
    But I think life without some worry or anxiety would be rather bad for us. I
    don't have an exact clue of why or how that would be, but I'm just certain that
    some of it is a good thing to have in ones life. To keep their body moving forward.
</p>

<p>So let's list out some things that worry me. Driving for example. It's a terrible worry
    to be in control over a vehicle. So I don't drive. Maybe that's not the kind of worry to be
    thinking about though, I'm not sure. I suppose any kind of worry can be any kind of worry.
    So that's a good example of worry that I have. I mean we're talking nightmares about driving
    that I have. Not just a natural fear or worry I guess then is it? No, I didn't think so.
    My driving instructor even said he was afraid I'd end up killing someone. I have that so
    ingrained in my mind that I can't bring myself to drive. Talk about a disabling worry/stressor.
</p>

<p>Sometimes just being in a car freaks me out. I can actually see accidents happening. They might
    fall under hallucinations though. I can see cars coming at us and crashing into us. So I try
    my best not to pay attention to anything when we're going someplace. Probably not the best
    thing to happen if we actually do get into an accident, I wouldn't be a good witness, but
    yeah that's just how things are. Also being behind the steering wheel of a car freaks me out bad.
    I feel trapped, unable to move.
</p>

