<p>Thoughts tend to get in the way a lot of the time. Let's face it, this life is full of thoughts.
    Some thoughts are are good, other thoughts are considered bad. Then there are the in-between
    thoughts that don't make any sense at all.
</p>

<p>Sometimes I don't mind the thoughts. They can do whatever they want in my mind. Then there are other
    times that I simply do not like the thoughts. They need to stop and I can't control them. I can't tell
    them enough that I need them to stop, they don't stop unfortunately and they run their course.
</p>

<p>So I deal with it. I live with it. I allow it to overtake me and my mind. Not the most healthiest
    way to deal with something mind you, but I don't know how else to deal.
</p>

<p>Intrusive thoughts tend to get in the way sometimes. They're relentless and don't stop. I wish I
    had some way to make them simply go away, but they seem to continue to come and don't want to
    leave me alone. So I deal with them. Do I deal with them in the most effective way possible? Probably
    not, but I do try my best.
</p>

<p>What's an intrusive thought? Well, let's look it <a href="https://www.verywellmind.com/intrusive-thoughts-7111540" target="_blank">up</a>.</p>

<blockquote cite="https://www.verywellmind.com/intrusive-thoughts-7111540">
Intrusive thoughts are unwanted, involuntary thoughts or images that suddenly enter your mind, 
often causing distress. They can be disturbing and may involve violent, sexual, or socially 
inappropriate content. These thoughts are typically shocking to the individual and have no 
bearing on their desires or reality. They are common and can affect many people, but they 
do not indicate a person's true intentions or character.
</blockquote>

<p>Ah thoughts that come into my mind and I just don't have anything to do with it. There are
    just so many thoughts that come into my head. I wish there was a way to silence them all.
    I don't need to be thinking the things that I think. They interfere with everything that
    I do, and are so annoying.
</p>

<p>Intrusive thoughts are automatic, something one doesn't have control over. It would be nice
    to be able to force them out for good. But I doubt that would be a thing. I mean is it okay
    to allow the thought to just exist and accept that it is there? I am not sure. I'd have to
    speak with someone about that.
</p>

<p>I suppose it's good to talk to a therapist about all of this. But I'm afraid of what they will
    say to me regarding the thoughts I have. It's not easy telling them anything about the thoughts
    I'm experiencing. I don't know why I feel scared about that, I mean I'm sure it's nothing they
    haven't heard before. It wouldn't shock or surprise them in the least. So why can't I say anything
    about what I think to them? A question for another time perhaps.
</p>

<p>It would be nice if I were to wake up one morning and not have any kind of intrusive thought in my
    head. That's an ideal situation. To simply wake up free of those weird terrible thoughts.
</p>

<p>I wonder what causes these thoughts to occur. There has to be some reason or thought for them right?
    I mean they just don't happen randomly one would think. Yeah I've no clue about any of it.
</p>

<p>Some thoughts stress me out and feed my anxiety even more. Those thoughts I could really do away
    with. They tend to eat away at me. It's not fun.
</p>

