<h2>Oh the things that come out of my mouth</h2>

<p>There are many things that come out of my mouth at times. They are not helpful things at all.
    But they are hurtful thoughts and phrases. "I want to die" and so forth.
</p>

<p>I'm working on making it so they don't do this to me. It would be nice to be able to simply
    get over the thought process and whatever else is bogging my mind down. It would be nice to
    be able to calm my mind and not whisper these thoughts out loud. People can hear when I do so
    and I get strange looks.
</p>

<p>I don't know why I say such things, why I whisper such things to myself. They are counter
    productive to life going forward. Sometimes I just whisper swear words under my breath.
    I tend to say the most vile swear words known to man. I shouldn't of course be doing this,
    it really isn't conducive to anything good. Yet I am but a man and do as I please as it
    were.
</p>

<p>Other phrases that come out of my mouth are "I hate life" and "when will it all end?" I'm not
    sure about either of these. They seem to come out from nowhere. I can't seem to pinpoint any
    of it down. It would be nice to be able to understand what's going on with all of it, but
    well I can't. It gets to be so frustrating in my mind/head when I go through an experience
    like this. I don't know why this is happening this way. Or why things just tend to happen.
    It's like life takes control and I don't have any. I'm sure people will say "Oh you have control
    over what you say! You always have control." That may be true, but I don't have control over what
    the voices say or the thoughts say that I mutter out. That control belongs to someone else, and I
    don't know who controls that.
</p>

<p>I didn't always whisper things. I remember when my mind was quiet and calm. Those were peaceful
    days. Nice days, good days. I wish I could have those days back. But I fear that it is going
    to be an impossible task. I don't know why that is, one would think I could just pick and choose
    what I want changed in my brain and bam I could do it. Unfortunate it doesn't work that way.
</p>

