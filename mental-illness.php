<h2>The Thoughts, They Continue</h2>

<p>I swear it's not all as bad as you think it is. It can actually
    be much worse. Where do I even begin? I'm not sure. Being certain about such things is an
    important part of this life. One would think it would be at least. I mean come on now, 
    you want to be sane. Doesn't everyone want to be sane?
</p>

<p>It doesn't always happen that way though. Unfortunately. There are some days that are better
    than others and other days that are worse than some. If you can believe it. Medications help
    you get through the hard days, I can attest to that fact.
</p>

<p>Sometimes these thoughts are <a href="suicidal-ideation.php">suicidal ideation</a>. That is, the 
    thought of suicide.
</p>

<h2>Chemical Imbalance</h2>

<p>So what exactly happens? Well it's a chemical imbalance in the brain. That's where it all 
    begins. You don't have control over that chemical imbalance. I mean it would be nice if
    you did right? Well you don't. You have to accept that, and you have to accept the fact
    that you'll be on <a href="medication.php">medications</a> to help stabilize that imbalance; 
    and you'll probably be on those medications for the rest of your life.
</p>

<p>Sometimes the medications stop working. So you have to switch it up a little. If that happens
    it's okay. Trust me, I've been there. It's okay to ask for help too. Those who love you are
    standing by waiting to help you. They won't think you're weird or that you're a creep or a
    loser. They have got your back.
</p>

<h2>Therapy</h2>

<p>Seeing a therapist has its advantages. They are there to listen to your problems and are able
    to help you figure out things to put in your toolbox to help you the next time you are feeling
    anxious, or <a href="suicide.php" class="red">suicidal</a>, or whatever. Anxiety is such
    a mind numbing thing to experience. Things kick in you weren't expecting and then you act on it.
    Or you freeze. either way, this life doesn't quite make perfect sense. These things that happen
    to us don't make sense either. We have to learn how our body and mind reacts to each of these
    in order to survive.
</p>

<p>Things that I had in my toolbox when I was going to a therapist were:</p>
<ul>
    <li>Meditation</li>
    <li>Breathing Exercises</li>
    <li>Journaling</li>
</ul>

<p>There were more, but I can't think of what they are at the moment.
    I think these were the ones that stuck out the most in my mind. They
    tend to work for me most of the time.
</p>

<h2>Hospitalization</h2>

<p>I've been hospitalized four times for mental illness. Each time the hospital
    staff were amazing. One time I went at night, and it was getting close to
    eight or nine p.m. the nurses were so kind. I remember one nurse that was
    very polite and patient with me. I was obviously having a difficult time
    and they could see that. Thank goodness they knew what to do with me.
</p>

<p>It wasn't an easy thing being in the hospital the first time. But I felt
    among my own people. Those who were also having problems like I was having.
    That's all there was to it. People who had genuine issues in life like I did.
    Some people had it worse than I did, some had it easier. Either way, we were all
    having problems that needed to be taken care of. Granted there is no permanent
    solution to any of this. But medications do help. As long as you stay on top of
    your medications and take them, the easier to deal with this illness is.
</p>

<h2>My Stay In The Mental Hospital</h2>

<p>So, not everyone can say they've stayed a week, let alone two, in the Behavioral Health 
    unit of a hospital. That's the place you go when you're having suicidal thoughts and possible actions.
</p>

<p>I never thought I would have done that myself, but well there I was sitting in the ER having 
    blood drawn and questions asked of me. How did I get to this place in my life, am I using drugs etc.
</p>

<p>The room I was in initially was void of any electrical equipment. Literally anything I could 
    have used to harm myself had been removed from the room. A security guard was waiting outside 
    the door in case I decided to bolt and make a run for it, or something. Basically he was there 
    to keep me safe.
</p>

<p>They made me put on a yellow hospital gown, which didn't have any strings. You'll get used to 
    the no strings at all thing in a moment. You couldn't have any strings at all. Shoelaces were 
    removed, any strings in PJs or Sweat Pants were cut out, belts were taken away. Any possible 
    chance of hanging yourself was out of the question.
</p>

<p>After talking with a social worker and letting them know I was admitting myself, I waited for
    what seemed like an eternity. I simply waited for a room to be made ready upstairs on the third floor.
</p>

<p>Once a room was made ready, I was taken to that third floor via an elevator only staff had 
    access to. It was a short elevator ride, that wasn't so bad.
</p>

<p>Once I got up to the third floor, more questions were asked. Some were duplicates from the 
    ones downstairs. Nope, still don't smoke, thanks for asking.
</p>

<p>As they were asking me these questions, they went through my belongings I brought to make 
    sure I didn't include something which could be considered a weapon. They put my clothes in my room.
</p>

<p>After the questions, I was taken to my room and was asked to disrobe in front of two nurses. 
    So naked I got. They made sure I didn't have any bruises anywhere etc. that my skin was devoid 
    of any kind of abuse, which it was.
</p>

<p>As it was late in the night, I went to bed, this was my second journey to the psych unit...I 
    slept good that night. I was safe, felt safe, nothing could get to me. It was a nice feeling.
</p>

<h2>The Music Still Plays</h2>

<p>Life comes at us when we really least expect it to. Something goes a bit wonky, 
    and we are left to put the pieces back together. The music simply continues to 
    play on without stopping. Life doesn't stop because you are having a difficult 
    time to get through something. Life doesn't just give up. It has to keep flowing 
    for all the other billions of people around the world. It would be quite odd if 
    everything just stopped because one person's heart gave way.
</p>

<p>So, life doesn't work that way. Good to know. What are we to do with the broken pieces 
    of a heart that's holding together with duct tape and hope? Can a person live off of 
    hope alone? I'm not sure. I imagine one could really wish that would work out...if it 
    does, great. If it doesn't? Then there could be other issues down the road. Who knows. 
    Whatever the case, life continues to move on. The music still plays.
</p>

