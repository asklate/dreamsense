and there was a world to share.
</blockquote>
        
<p>The dream begins as it always does. I'm swimming in a pool. A man enters and
points a gun at me. Before I can reach for my own weapon, he fires two rounds
into my chest killing me. I wake up in a startled mess. My sheets are soaked
from sweat. I live another day only to have the dream again another night.</p>
        
<p>After waking up, I have no recollection of why the bad guy was
after me. It makes no sense. I have no clues except for what's in the dream
itself.</p>
        
<p>What if we are on this earth at the expense of ourselves. People are watching
us and making jokes, exchanging money on how our day will turn out. We are not
alone in this life. People are watching. They watch our every movement to see
if we will do as they expect us to do.</p>
        
<p>You could consider it a gamble. A game these people are playing, but they
    aren't playing with chips to be won at a casino. No, they are playing with
    people's lives.</p>
        
<p>In my dream I get tossed into the river. If I'm lucky the dream doesn't do
    a reset and I wake up. However there are times when I wake up wihin the dream.
    The events play out again esactly as they did the first, and I wake up a second
    time within the dream. You could say it's Inception. When I finally wake up, I
    have to question if this is real life or if I am still in the dream.</p>
        
<p>If I could simply forget the dream and never have it resurface that
    would be ideal. But the dream never goes away. I can't even begin to tell
    you how long I've had this dream night after night for a few days. It goes
    away for a little while, but then it comes back. Always the same story.
    Always the same outcome.</p>
        
<blockquote>
    To touch that which is real<br/>
    only to find out it's false,<br/>
    the dream will catch you<br/>
    and take you in.
</blockquote>
        
<p>I often wonder if I shouldn't dwell on the dream. If the dream will
    just be there. If it's always going to be there, why should I dwell
    on whatever it is? Doesn't quite make sense I know, but maybe it will
    eventually.</p>
        
<p>The dream never makes any sense. I can dream it over and over again
    and it will never make sense to me. It's something I've come use to.
    Something I will simply have to live with and accept as time goes on.</p>
        
<p>What if the dream isn't real? What if it's just something that makes
    me think it's real? There are so many possibilities out there regarding
    the different dreams I have. I doubt I'll ever get through them all.</p>
        
<blockquote>
    Touch the sky and see where it takes you. Dream the impossible and see
    what happens.
</blockquote>
        
<p>Dreams are different for everyone. Sometimes a dream can mean nothing
    at all. Other times a dream can mean something very real and have a deeper
    meaning than face value.</p>
        
<p>Life has a purpose and a plan to go along with it. Your dreams can be
