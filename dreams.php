<h2>Making Sense of the Dream</h2>
            
<p>Welcome. there are many things in this life that I would like to try and
figure out. It's not always an easy journey though. Sometimes we come and
go as we please, yet we really do not have the answers we are searching
for.</p>
    
<p>Lots of people have dreams. Ambition. Aspirations they wish to accomplish
before they kick the bucket or leave this world as it were. I suppose that's
why they call it a bucket list. You kick the bucket and bam you're gone.</p>
        
<p>The dream belongs in the past. There is no current dream to belong to.</p>
        
<blockquote>
Had I a dream, I would share it with<br/>
everyone out there in the world<br/>
