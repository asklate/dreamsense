<p>Ah yes, that part of life where we cross over to the other side.
    People are afraid to die. Why is that? Why be afraid to die when
    there's an adventure just beyond the horizon to be had? Are we so
    locked in this mortal coil that we just don't know what's out there
    on the other side of life? That we are afraid to die? Personally
    I would welcome it.
</p>

<p>Now let me clarify, I'm not ready to up and end my life.
    There's no reason for that. Just so we're clear on that fact.
    But it would be interesting to see what's on the other side of the
    curtain as it were. Where those who <em>have</em> passed on live to
    see what it's like. Just a small vacation as it were then I'd be back.
    Unfortunately it doesn't work that way.
</p>

