    The synonyms are interesting at best. Death is an interesting thing to study.
    The end of something. Something great? Maybe. Something terrible? Also a maybe.
    Either way, death is the stopping/end of something. It's funny, everyone goes
    through this process. No one can escape it. No matter how hard you try, there's
    no machine or process that will actually make you live longer. So why try and make it
    so you are able to do so? There's no reason to. Do people want to cling onto life so much
    that they are willing to just let go of what's on the other side? What if there are loved
    ones who they haven't seen or actually met there? Wouldn't you want to go there to talk with
    and learn from them? I think it would be fascinating.
</p>

<p>Everyone has their own thought process of course. So it's up to you.</p>

<p>I find myself jealous about those who have passed on. I know it's kinda a crazy thing to be jealous
    about for sure. I want to be on that other side so bad some days. Like I said above, I'm not willing
    to off myself in order to get there. No that would be a bad thing. Something that isn't new to me
    of course, but something I'm just not willing to do.
</p>

<p>When my dad passed away, I felt alone in the world. If my wife hadn't of been there, I don't know
    what I would have done. She helped me through it, and is still helping me through the grief process.
