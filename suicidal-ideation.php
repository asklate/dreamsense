<p>The thoughts come a little easily some days, other days they aren't that easy.</p>

<p>The following quote comes from <a href="https://my.clevelandclinic.org/health/symptoms/suicidal-ideation" target="_blank">Cleveland Clinic</a>.</p>

<blockquote cite="https://my.clevelandclinic.org/health/symptoms/suicidal-ideation">
Suicidal ideation (suicidal thoughts) are thoughts or ideas centered around death or suicide. 
Experiencing suicidal ideation doesn’t mean you’re going to kill yourself, but it can be a warning sign.
</blockquote>

<p>Throwing myself in front of a moving car, jumping out of a moving car, hanging myself. I have
    thought about each of these from one time to another. Throwing myself down a stairwell in an
    office building is another example. Sounds easy doesn't it? Just simply allow it to happen.
    Doesn't mean it will always happen, but someday it might. You just never know.
</p>

<p>They all feel perfectly natural to me. Deep down inside I know they are wrong and I shouldn't
    allow them to affect my thought process on life, yet here they are. They continue to mess with
    my mind and there's nothing I am able to do about it. I'm sure there is something I <em>could</em>
    do about it, but for right now? No, I don't dare venture into that dark place of my mind. It's too
    scary to even go there.
</p>

<p>This morning I had a thought of going back to the mental hospital and hanging myself with a bed sheet
    that they provide. Just plop it over the door, close the door and get my neck all tied up in order
    to kill myself. Again, not a good plan at all.
</p>

<p>I do wonder if I'm flirting with death too much with these thoughts. I'm not certain of what to do
    with any of them. But you know how that can be, I think. Hell, I don't even know if I understand
    what's going on most days myself.
</p>

<p>I wish I didn't have this problem in my life. It would be nice to be able to go through a day
    without thinking, oh I could kill myself by doing x y or z. Yeah that would be nice to simply
    give up on it all.
</p>

