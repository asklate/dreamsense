<p>I want to say it first began in High School when I thought I was 
    trying to beat the devil. Now when I say I was "trying to beat the devil", I mean I 
    actually thought I was hearing the devil speak to me at night and that I had to find 
    some way to overcome him. Looking back I should have sought professional help then. I remember
    telling a friend about this experience, and he looked at me like I was nuts. Talk about craziness
    right? Yeah something like that.
</p>

<p>I believe my first mental break was when I was serving a mission for <a href="https://lds.org" target="_blank">The 
    Church of Jesus Christ of Latter-Day Saints</a>. I wanted to go home so bad. It could have just been 
    homesickness. But...there was something more to it. About halfway through I was just done with it all. 
    I had had enough of rejection and wanted to go home. But I was afraid that I was unable to because I
    would simply be told <em>no</em>. I know that sounds silly to people. But that's what happened.
    I didn't bring it up to anyone and decided to keep on moving forward in life. I dare say this was
    a slight mental break and not a full blown one. If that makes sense.
</p>

<p>I had a mental break around January of 2020. Talk about a crazy time. I think I have an idea
    of what took me to the breaking point? But I'd rather not talk about it. I began hearing voices.
    They weren't nice voices either. Mostly voices telling me to kill myself. When I would ask a
    doctor why the voices don't like me, they didn't have a response for me at all.
</p>

<p>It would have been nice if they would have had a reason behind it all. I mean aside from being
    diagnosed with the disorder. But hey I guess you just take whatever life hands out to you. It
    can be a bit unnerving at times. I won't lie. A lot of the times I just hear my name being called
    out. Again no clue why I have to deal with this disorder, I just know that I do and that is that.
</p>

<p>I know I do better when I'm in counseling sessions. But with the amount of classes I would have
    to go to, I wouldn't be able to handle my job and stay on top of things. So I've decided to
    forgo going to counseling and just keep on the medications that I have been on and go from
    there. That's just how this life pans out at times I suppose.
</p>

<p>I've been hospitalized four times for this illness. I remember one time the voices in my head
    got so bad that they were screaming at each other. I had to scream at them to make them stop.
    I remember rushing to the nurse on duty and asking her what the hell was going on. Was I losing
    my mind? She explained that I could be hallucinating and hearing voices. I was so scared about
    what was happening. I asked her to come out of her little confined area so she could talk to
    me. That was comforting.
</p>

<p>Another time during my hospitalization, I saw a nun walk into my room. I followed her. I thought
    it odd that there was a nun there. Because there were no nuns at the hospital at the time I 
    was there. They didn't have any on staff. She had a clipboard with her. As I entered my room,
    she looked at me and apologized for being there. She excused herself. I went and followed. She had
    disappeared. I don't know where she went. So visual hallucinations are also possible.
</p>

<p>It's funny every time I end up in the hospital, the lady that checks me in asks how I'm doing.
    It's like, I just had very suicidal thoughts...and am hearing things.
    How do you think I'm doing? I usually just say fine. We both know I'm indeed not fine at that
    point. I remember one time a guy walked in. He had bandages on his wrists. There was no need
    to ask why he was in there.
</p>

<p>Being raised in a very religious family, I have to wonder at times <em>why</em> I have these
    problems. Are they God's work? Are they the devil's doing? I'm no longer religious, that is I
    don't go to a church anymore. But I still find myself wondering these very questions. I doubt
    going to church would help me answer these questions. I know the answer I would be given.
    That we each have trials in this life and we must find a way to overcome them. Or something like
    that. I doubt God would have given me this illness. Like my anxiety, it's a chemical imbalance 
    in my brain. I was born like this.
</p>

<p>Medications can make you sleepy. So listen to the doctor or the pharmacist on
    when the best time to take the medication is. (Note: I am not a doctor of any kind and I am
    <em>not</em> giving you medical advice.)
</p>

<p>People don't always believe you when you say you have issues of this nature. They just say
    that you're sad or something. It's difficult to explain to them that no that's not what's going
    on at all, and it doesn't have to be something completely out of the ordinary or out there 
    to be considered schizoaffective. Look it up, I have it typed out <a href="easy.php">here</a>. 
    Yeah that's what the definition is. Sometimes the hearing of things or saying things out loud to 
    yourself over and over again can be disturbing in and of itself. As an example, I find myself
    whispering to myself "<a href="whispers.php">I want to die</a>", I have to stop myself at times and 
    remind myself not to say that. Part of Schizoaffective is depression is it not? Yeah that's what 
    I thought. People seem to forget that. It points out major depressive episodes. I have those for 
    sure. The doctor has indicated that in their report about me.
</p>

<p>Sometimes the <a href="demons.php">demons</a> come and you don't even realize it. You just 
    accept it. Not saying that's the best way to deal with your demons, but well it happens.
    It's just something that comes along and you just have to deal with it. Fortunately there
    are ways to fight the demons, you are not alone in your journey. You are never alone. Each
    person fights their demons in a different way, because not every person will react the same
    way so there has to be many different ways to fight the demons inside.
</p>

<p>At times I wonder if just ignoring the demons in my head would work. I'm not sure how that would
    happen. Or if it would even work. It's a thought at least. Might not be a helpful thought, but
    it's a start.
</p>

